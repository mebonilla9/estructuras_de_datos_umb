  private void validarExpresion(String text) {
    Stack<String> pila = new Stack<>();
    String[] cadena = text.split(Pattern.quote(""));
    for (String valor : cadena) {
      if (pila.isEmpty() && valor.equals(CIERRE)) {
        JOptionPane.showMessageDialog(
          rootPane,
          "La expresion escrita no esta balanceada",
          "La expresion no debe iniciar con parentesis de cierre!",
          JOptionPane.ERROR_MESSAGE
        );
        return;
      }
      if (valor.equals(APERTURA)) {
        pila.push(valor);
      } else if (valor.equals(CIERRE)) {
        pila.pop();
      }
    }
    JOptionPane.showMessageDialog(
      rootPane,
      pila.isEmpty() ? "La expresion escrita se encuentra balanceada" : "La expresion escrita no esta balanceada",
      pila.isEmpty() ? "Expresion valida" : "Expresion no valida",
      pila.isEmpty() ? JOptionPane.INFORMATION_MESSAGE : JOptionPane.ERROR_MESSAGE
    );
  }

